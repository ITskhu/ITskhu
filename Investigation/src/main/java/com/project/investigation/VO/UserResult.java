package com.project.investigation.VO;

public class UserResult {

	private int stateSeq;
	private int empno;
	private double totalAvg;
	private String itemsAvg;
	public int getStateSeq() {
		return stateSeq;
	}
	public void setStateSeq(int stateSeq) {
		this.stateSeq = stateSeq;
	}
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public double getTotalAvg() {
		return totalAvg;
	}
	public void setTotalAvg(double totalAvg) {
		this.totalAvg = totalAvg;
	}
	public String getItemsAvg() {
		return itemsAvg;
	}
	public void setItemsAvg(String itemsAvg) {
		this.itemsAvg = itemsAvg;
	}


}
